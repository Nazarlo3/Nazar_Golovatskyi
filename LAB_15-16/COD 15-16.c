#include <stdio.h>

int main() {
    int arr[10]; // оголошення масиву розміром 10
    int sum = 0; // змінна для зберігання суми елементів

    // заповнення масиву довільними цілими числами, введеними з клавіатури
    printf("Введіть 10 цілих чисел:\n");
    for (int i = 0; i < 10; i++) {
        scanf("%d", &arr[i]);
    }

    // обчислення суми елементів масиву
    for (int i = 0; i < 10; i++) {
        sum += arr[i];
    }

    // виведення масиву та суми його елементів на екран
    printf("Масив: ");
    for (int i = 0; i < 10; i++) {
        printf("%d ", arr[i]);
    }
    printf("\nСума елементів: %d\n", sum);

    return 0;
}
